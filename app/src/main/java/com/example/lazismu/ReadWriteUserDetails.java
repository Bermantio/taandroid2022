package com.example.lazismu;

public class ReadWriteUserDetails {
    public String namalengkap, jeniskelamin, alamat, telepon, profesi;
    public ReadWriteUserDetails(String namalengkap, String jeniskelamin, String alamat, String telepon, String profesi) {
        this.namalengkap = namalengkap;
        this.jeniskelamin = jeniskelamin;
        this.alamat = alamat;
        this.telepon = telepon;
        this.profesi = profesi;
    }
}
